import sys
from dataclasses import dataclass
from typing import TypeAlias


@dataclass
class Node:
    state: str
    parent: "Node | None"
    action: list[int]


@dataclass
class StackFrontier:
    frontier: list[Node]

    def add(self, n: Node):
        self.frontier.append(n)

    def empty(self):
        return len(self.frontier) == 0

    def exists(self, n: Node):
        return any(node.state == n.state for node in self.frontier)

    def peek(self):
        return self.frontier[-1]

    def pop(self):
        if self.empty():
            raise Exception("Frontier empty!")

        return self.frontier.pop()


class QueueFrontier(StackFrontier):
    def remove(self):
        if self.empty():
            raise Exception("Frontier empty!")

        return self.frontier.pop(0)


Point: TypeAlias = tuple[int, int]


@dataclass
class Maze:
    height: int
    width: int
    walls: list[list[bool]]
    solution: list[list[Point]] | None
    start: Point
    goal: Point

    explored: list[Point]
    num_explored: int

    def __init__(self, filename: str):
        # Read file and set height and width of maze
        with open(filename) as f:
            contents = f.read()

        # Validate start and goal
        if contents.count("A") != 1:
            raise Exception("maze must have exactly one start point")
        if contents.count("B") != 1:
            raise Exception("maze must have exactly one goal")

        # Determine height and width of maze
        contents = contents.splitlines()
        self.height = len(contents)
        self.width = max(len(line) for line in contents)

        # Keep track of walls
        self.walls = []
        for i in range(self.height):
            row: list[bool] = []
            for j in range(self.width):
                try:
                    if contents[i][j] == "A":
                        self.start = (i, j)
                        row.append(False)
                    elif contents[i][j] == "B":
                        self.goal = (i, j)
                        row.append(False)
                    elif contents[i][j] == " ":
                        row.append(False)
                    else:
                        row.append(True)
                except IndexError:
                    row.append(False)
            self.walls.append(row)

        self.solution = None

    def print(self):
        solution = self.solution[1] if self.solution is not None else None
        print()
        for i, row in enumerate(self.walls):
            for j, col in enumerate(row):
                if col:
                    print("â–ˆ", end="")
                elif (i, j) == self.start:
                    print("A", end="")
                elif (i, j) == self.goal:
                    print("B", end="")
                elif solution is not None and (i, j) in solution:
                    print("*", end="")
                else:
                    print(" ", end="")
            print()
        print()

    def neighbors(self, state: Point) -> list[tuple[str, Point]]:
        row, col = state
        candidates = [
            ("up", (row - 1, col)),
            ("down", (row + 1, col)),
            ("left", (row, col - 1)),
            ("right", (row, col + 1)),
        ]

        result: list[tuple[str, Point]] = []
        for action, (r, c) in candidates:
            if 0 <= r < self.height and 0 <= c < self.width and not self.walls[r][c]:
                result.append((action, (r, c)))
        return result

